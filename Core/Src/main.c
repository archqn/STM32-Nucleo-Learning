int main(void)
{

  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_USART2_UART_Init();



  printf("\r\n=== STM32 USART2 Debug Start ===\r\n");

  char msg[] = "How you like dem apples?\r\n";

  while (1)
  {
    HAL_StatusTypeDef status = HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
        
    if (status != HAL_OK)
    {
        printf("UART Transmission Failed!\r\n");
        Error_Handler();
    }

    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5);
    HAL_Delay(1000);
  }
}

